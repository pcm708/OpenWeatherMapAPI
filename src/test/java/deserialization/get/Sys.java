package deserialization.get;

public class Sys {

	private String type;
	private String id;
	private String country;
	private String sunrise;
	private String sunset;
	
	public String getType() {
		return type;
	}
	public String getId() {
		return id;
	}
	public String getCountry() {
		return country;
	}
	public String getSunrise() {
		return sunrise;
	}
	public String getSunset() {
		return sunset;
	}
	
}
