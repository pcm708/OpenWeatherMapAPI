package deserialization.getWeather;

public class Wind {

	private String speed;
	private String deg;
	private String gust;
	
	public String getGust() {
		return gust;
	}
	public void setGust(String gust) {
		this.gust = gust;
	}
	
	public String getSpeed() {
		return speed;
	}
	public String getDeg() {
		return deg;
	}
	
	
	
}
